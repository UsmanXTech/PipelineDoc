const githubClient = require('./client');

/**
 * Posts failure diagnosis details as a markdown comment on a GitHub PR.
 */
async function postPRDiagnosisComment({ owner, repo, prNumber, diagnosis, blame, flakiness }) {
  const commentBody = `
## 🚨 PipelineDoc Failure Doctor Diagnosis

### 🩺 Summary: **${diagnosis.summary || 'Pipeline failure detected.'}**

* **Failure Type:** \`${diagnosis.failure_type || 'unknown'}\`
* **AI Confidence:** \`${diagnosis.confidence || 0}%\`
* **Blame Attribution:** ${blame ? `${blame.author_name} (${blame.blame_confidence}% confidence)` : 'Unassigned'}
* **Flakiness Warning:** ${flakiness && flakiness.is_flaky ? `⚠️ Flaky Test detected (${flakiness.flaky_confidence}% confidence)` : 'None'}

---

### 💡 Suggested Fixes:
${(diagnosis.fixes || []).map((f, i) => `${i + 1}. ${f}`).join('\n')}

<details>
<summary>🔍 View Technical Details & Root Cause</summary>

#### Root Cause:
\`\`\`
${diagnosis.root_cause}
\`\`\`

#### Diagnostics Meta:
* Affected Module: \`${diagnosis.affected_file || 'N/A'}\`
* Line Reference: \`${diagnosis.affected_line || 'N/A'}\`

</details>

_Generated by PipelineDoc Failure Doctor._
`;

  try {
    const result = await githubClient.createPRComment(owner, repo, prNumber, commentBody);
    console.log(`Successfully posted diagnosis comment on PR #${prNumber}`);
    return result;
  } catch (error) {
    console.error(`Failed to post PR diagnosis comment on PR #${prNumber}:`, error.message);
    throw error;
  }
}

module.exports = {
  postPRDiagnosisComment
};
